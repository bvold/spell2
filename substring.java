class substring {
				public static void main (String[] argv) {
					String test_string = "notorious";
					System.out.println("test_string =" + test_string);
					System.out.println("test_string.substring(0,1) = " + test_string.substring(0,1));
					System.out.println("test_string.substring(7,9) = " + test_string.substring(7,9));
					System.out.println("test_string.substring(0,9) = " + test_string.substring(0,9));
				}
}
